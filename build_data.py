def main():
    print("Building deorphanization data files...")
    print()

    # 1. raw_data.json
    raw_data = []
    raw_data.extend(build_raw_data())
    raw_data.extend(build_raw_data_part2())
    raw_data.extend(build_raw_data_part3())
    raw_data.extend(build_raw_data_part4())
    raw_data.extend(build_raw_data_part5())
    raw_data.extend(build_raw_data_part6())

    with open(os.path.join(OUTPUT_DIR, "raw_data.json"), "w") as f:
        json.dump(raw_data, f, indent=2)
    print(f"1. raw_data.json: {len(raw_data)} receptor-ligand pairs written")

    # 2. orphan_receptor_catalog.json
    orphan_cat = []
    orphan_cat.extend(build_orphan_catalog())
    orphan_cat.extend(build_orphan_catalog_part2())

    with open(os.path.join(OUTPUT_DIR, "orphan_receptor_catalog.json"), "w") as f:
        json.dump(orphan_cat, f, indent=2)
    print(f"2. orphan_receptor_catalog.json: {len(orphan_cat)} orphan receptors cataloged")

    # 3. ligand_classification.json
    ligand_classes = build_ligand_classification()

    with open(os.path.join(OUTPUT_DIR, "ligand_classification.json"), "w") as f:
        json.dump(ligand_classes, f, indent=2)
    print(f"3. ligand_classification.json: {len(ligand_classes)} ligand families identified")

    # 4. receptor_classification.json
    receptor_classes = build_receptor_classification()

    with open(os.path.join(OUTPUT_DIR, "receptor_classification.json"), "w") as f:
        json.dump(receptor_classes, f, indent=2)
    print(f"4. receptor_classification.json: {len(receptor_classes)} receptor families identified")

    print()
    print("All files generated successfully.")


if __name__ == "__main__":
    main()
